package com.jincity.rss.magazine;

public class Magazine {

	private String magazineJID = "";// 期刊的唯一标志
	private String magazineName = "";// 期刊的名称

	public String getMagazineJID() {
		return magazineJID;
	}

	public void setMagazineJID(String magazineJID) {
		this.magazineJID = magazineJID;
	}

	public String getMagazineName() {
		return magazineName;
	}

	public void setMagazineName(String magazineName) {
		this.magazineName = magazineName;
	}

}
