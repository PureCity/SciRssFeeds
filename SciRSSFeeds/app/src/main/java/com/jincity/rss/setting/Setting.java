package com.jincity.rss.setting;

public class Setting {

	private int useOwnBro;// 使用内置浏览器
	private int showImageInWifi; // 仅在wifi下显示图片
	private int showPageCar;// 显示页面动画
	private int showPageChage;// 显示翻页效果
	private int showImageChoise;// 显示图片选项
	private int useBigWord;// 使用大字体
	private int UpdateAuto;// 自动更新
	private int ClearAuto;// 自动清除

	public int getUseOwnBro() {
		return useOwnBro;
	}

	public void setUseOwnBro(int useOwnBro) {
		this.useOwnBro = useOwnBro;
	}

	public int getShowImageInWifi() {
		return showImageInWifi;
	}

	public void setShowImageInWifi(int showImageInWifi) {
		this.showImageInWifi = showImageInWifi;
	}

	public int getShowPageCar() {
		return showPageCar;
	}

	public void setShowPageCar(int showPageCar) {
		this.showPageCar = showPageCar;
	}

	public int getShowPageChage() {
		return showPageChage;
	}

	public void setShowPageChage(int showPageChage) {
		this.showPageChage = showPageChage;
	}

	public int getShowImageChoise() {
		return showImageChoise;
	}

	public void setShowImageChoise(int showImageChoise) {
		this.showImageChoise = showImageChoise;
	}

	public int getUseBigWord() {
		return useBigWord;
	}

	public void setUseBigWord(int useBigWord) {
		this.useBigWord = useBigWord;
	}

	public int getUpdateAuto() {
		return UpdateAuto;
	}

	public void setUpdateAuto(int updateAuto) {
		UpdateAuto = updateAuto;
	}

	public int getClearAuto() {
		return ClearAuto;
	}

	public void setClearAuto(int clearAuto) {
		ClearAuto = clearAuto;
	}

}
